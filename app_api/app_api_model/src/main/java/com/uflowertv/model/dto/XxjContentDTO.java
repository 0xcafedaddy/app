package com.uflowertv.model.dto;

public class XxjContentDTO {

	private String content_id;//内容包ID
	private String content_name;//内容包名称
	private String content_status;//内容包状态
	private String content_introduce;//内容包介绍
	private String video_list;//视频列表
	public String getContent_id() {
		return content_id;
	}
	public void setContent_id(String content_id) {
		this.content_id = content_id;
	}
	public String getContent_name() {
		return content_name;
	}
	public void setContent_name(String content_name) {
		this.content_name = content_name;
	}
	public String getContent_status() {
		return content_status;
	}
	public void setContent_status(String content_status) {
		this.content_status = content_status;
	}
	public String getContent_introduce() {
		return content_introduce;
	}
	public void setContent_introduce(String content_introduce) {
		this.content_introduce = content_introduce;
	}
	public String getVideo_list() {
		return video_list;
	}
	public void setVideo_list(String video_list) {
		this.video_list = video_list;
	}
	
	
}
